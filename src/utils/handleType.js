export const handleChangeColour = (type) => {
  if (type === "bug") {
    return "#729f3f";
  }
  if (type === "dark") {
    return "#6a7064";
  }
  if (type === "dragon") {
    return "#f16e57";
  }
  if (type === "electric") {
    return "#f16e57";
  }
  if (type === "fairy") {
    return "#fdb9e9";
  }
  if (type === "fighting") {
    return "#d56723";
  }
  if (type === "flying") {
    return "#bdb9b8";
  }
  if (type === "ghost") {
    return "#7b62a3";
  }
  if (type === "grass") {
    return "#9ccd51";
  }
  if (type === "ground") {
    return "#ab9842";
  }
  if (type === "ice") {
    return "#51c4e7";
  }
  if (type === "normal") {
    return "#a4acaf";
  }
  if (type === "poison") {
    return "#b97fc9";
  }
  if (type === "psychic") {
    return "#f366b9";
  }
  if (type === "rock") {
    return "#a38c21";
  }
  if (type === "steel") {
    return "#9eb7b8";
  }
  if (type === "water") {
    return "#4592c4";
  }
  if (type === "fire") {
    return "#fd7d24";
  } else {
    return "white";
  }
};
